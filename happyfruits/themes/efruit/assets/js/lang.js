function translate(text, id, language_code){
    if(typeof language_code == 'undefined')
        language_code = $('html').attr('lang');
    if (typeof language_code == 'undefined' || language_code != 'en')
        return text;
    else{
        var key = text;
        if (typeof id != 'undefined')
            key += id;
        if (typeof lang_table[language_code] != 'undefined' && lang_table[language_code][key])
            return lang_table[language_code][key];
        else
            return text;
    }
}